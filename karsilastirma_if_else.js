let yarko = 35;
let selin = 16;

if (yarko > selin) {
  console.log("Yarko'nun plakası Selin'in plakasından büyüktür.");
} else if (yarko == selin) {
  console.log("Selin'in plakası Yarko'nun plakasına eşittir.");
} else {
  console.log("Selin'in plakası Yarko'nun plakasından küçüktür.");
}
